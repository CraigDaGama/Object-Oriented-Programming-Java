public class CharDataType {
    public static void main(String args[]) {
        char x = 'a';
        System.out.println("before x++ x=" + x);
        x++;
        System.out.println("after x++=" + + x);
        x--;
        System.out.println("after x--=" + + x);
        System.out.printf("%d",(int)x);
    }
}
